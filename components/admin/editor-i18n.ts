export type EditorLanguage = "zh-CN" | "en";

export const editorLanguageStorageKey = "personal-site-studio:editor-language";

export const editorLanguageOptions: { code: EditorLanguage; label: string }[] = [
  { code: "zh-CN", label: "简体中文" },
  { code: "en", label: "English" }
];

export function resolveInitialEditorLanguage(): EditorLanguage {
  if (typeof window === "undefined") return "en";

  const saved = window.localStorage.getItem(editorLanguageStorageKey);
  if (isEditorLanguage(saved)) return saved;

  const browserLanguage = window.navigator.language.toLowerCase();
  return browserLanguage.startsWith("zh") ? "zh-CN" : "en";
}

export function isEditorLanguage(value: unknown): value is EditorLanguage {
  return value === "zh-CN" || value === "en";
}

